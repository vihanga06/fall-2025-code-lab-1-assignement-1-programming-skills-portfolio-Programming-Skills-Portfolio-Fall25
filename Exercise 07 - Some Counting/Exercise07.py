# Counting from 0 to 50 with increments of 1
for i in range(51):
  print(i)

# Counting down from 50 to 0 with decrements of 1
for i in range(50, -1, -1):
  print(i)

# Counting from 30 to 50 with increments of 1
for i in range(30, 51):
  print(i)

# Counting down from 50 to 10 with increments of 2
for i in range(50, 9, -2):
  print(i)

# Counting from 100 to 200 with increments of 5
for i in range(100, 201, 5):
  print(i)